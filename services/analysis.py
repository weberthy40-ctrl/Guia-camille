def _attach_rune_icons(runes):
    out = {}
    for key, values in runes.items():
        if isinstance(values, list):
            if key == "shards":
                out[key] = values
            else:
                out[key] = [{"nome": r, "icon": RUNE_ICONS.get(r, "")} if not isinstance(r, dict) else r for r in values]
        else:
            out[key] = values
    return out

def _attach_item_icons_to_block(block):
    block["item_inicial_icon"] = ITEM_ICONS.get(block["item_inicial"], "")
    block["situacionais"] = [{"nome": i, "icon": ITEM_ICONS.get(i, "")} if not isinstance(i, dict) else i for i in block["situacionais"]]
    return block

def _finalize_matchup(data):
    data["runas_padrao"] = _attach_rune_icons(data["runas_padrao"])
    data["runas_confronto"] = _attach_rune_icons(data["runas_confronto"])
    data["runas_recomendadas"] = _attach_rune_icons(data["runas_recomendadas"])
    data["itens"] = _attach_item_icons_to_block(data["itens"])
    return data

